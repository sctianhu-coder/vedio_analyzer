# 天气、风向、风力配置
WEATHER_TYPES = ["晴", "多云", "阴", "小雨", "中雨", "大雨", "雷阵雨", "小雪", "中雪", "大雾"]
WIND_DIRECTION = ["东风", "南风", "西风", "北风", "东北风", "东南风", "西北风", "西南风"]
WIND_POWER = ["1级", "2级", "3级", "4级", "5级", "6级", "7级"]
AIR_QUALITY = ["优", "良", "轻度污染", "中度污染", "重度污染"]

# =============================================
# 生成模拟天气数据
# =============================================
def generate_weather_data():
    start = datetime.strptime(START_DATE, "%Y-%m-%d")
    total_rows = 0

    with open(OUTPUT_FILE, "w", newline="", encoding="utf-8") as f:
        writer = csv.writer(f)
        # 写入表头
        writer.writerow(["日期", "城市", "省份", "最高温(℃)", "最低温(℃)", "湿度(%)",
                         "风向", "风力", "天气状况", "空气质量"])

        # 按天生成
        for day in range(DAYS):
            current_date = start + timedelta(days=day)
            date_str = current_date.strftime("%Y-%m-%d")

            # 随机抽取城市
            sample_cities = random.sample(CITY_DATA, min(CITIES_PER_LOAD, len(CITY_DATA)))

            for city, province in sample_cities:
                # 按季节/地区生成合理温度
                month = current_date.month
                if province in ["海南", "广东", "广西"]:
                    temp_high = random.randint(20, 36)
                    temp_low = random.randint(15, 26)
                elif province in ["黑龙江", "吉林", "内蒙古"]:
                    temp_high = random.randint(-10, 28)
                    temp_low = random.randint(-25, 15)
                else:
                    temp_high = random.randint(-5, 35)
                    temp_low = random.randint(-15, 25)

                # 其他字段
                humidity = random.randint(30, 95)
                wind_dir = random.choice(WIND_DIRECTION)
                wind_power = random.choice(WIND_POWER)
                weather = random.choice(WEATHER_TYPES)
                air_quality = random.choice(AIR_QUALITY)

                writer.writerow([date_str, city, province, temp_high, temp_low, humidity,
                                 wind_dir, wind_power, weather, air_quality])
                total_rows += 1

    print(f"✅ 数据生成完成！")
    print(f"📊 总条数：{total_rows:,} 行")
    print(f"📁 输出文件：{OUTPUT_FILE}")
    print(f"🚀 可直接上传 HDFS：hdfs dfs -put {OUTPUT_FILE} /test/")

if __name__ == "__main__":
    generate_weather_data()